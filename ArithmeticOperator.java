package operators;

public class ArithmeticOperator
{

	public static void main(String[] args) 
	{
	
        int a=10,b=3;
        int resultAddition=a+b;
        int resultSubtraction=a-b;
        int resultDivision=a/b;
        int resultMultiplication=a*b;
        int resultModDivision=a%b;
        
        
        System.out.println("Addition Operator: "+resultAddition);
        System.out.println("Substraction Operator: "+resultSubtraction);
        System.out.println("Divisionion Operator: "+resultDivision);
        System.out.println("Multiplication Operator: "+resultMultiplication);
        System.out.println("Modulus Division Operator: "+resultModDivision);
        
	}

}
